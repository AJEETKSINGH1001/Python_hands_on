'''
Python is a highly object-oriented language.
In Python, each and every element in a Python program is an object of one or the other class. A number, string, list, dictionary etc.
used in a program they are objects of corresponding built-in classes.
'''

'''
__dict__ − Dictionary containing the class's namespace.

__doc__ − Class documentation string or none, if undefined.

__name__ − Class name.

__module__ − Module name in which the class is defined. This attribute is "__main__" in interactive mode.

__bases__ − A possibly empty tuple containing the base classes, in the order of their occurrence in the base class list.
'''

